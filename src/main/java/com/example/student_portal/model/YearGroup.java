package com.example.student_portal.model;

/**
 * Enum representing the possible year groups for students at Bromsgrove School.
 *
 * Year 9 through Year 13 are included.
 */
public enum YearGroup {
    YEAR_9,   // Year 9
    YEAR_10,  // Year 10
    YEAR_11,  // Year 11 (usually GCSE year)
    YEAR_12,  // Lower sixth form
    YEAR_13   // Upper sixth form
}